"""Per-prompt evaluator: generate + loss for a single model."""

from __future__ import annotations

from ..inference.inference import InferenceService
from ..inference.loaded_model import LoadedModel


class EvaluatorResult:
    """Result of evaluating a single prompt on one model.

    Parameters
    ----------
    generated_text : str
        Text generated by the model for this prompt.
    avg_loss : float
        Average cross-entropy loss across the prompt tokens.
    perplexity : float
        Perplexity (``exp(avg_loss)``).
    """

    def __init__(self, generated_text: str, avg_loss: float, perplexity: float) -> None:
        self.generated_text = generated_text
        self.avg_loss = avg_loss
        self.perplexity = perplexity


class Evaluator:
    """Evaluates a single model on a single prompt.

    Combines ``InferenceService.generate`` (text samples) and
    ``InferenceService.loss_breakdown`` (per-token loss) into one
    per-prompt evaluation step.

    Parameters
    ----------
    inference : InferenceService
        Inference service for loading models, generating, and computing loss.
    """

    def __init__(self, inference: InferenceService) -> None:
        self._inference = inference

    def evaluate_prompt(
        self,
        prompt: str,
        *,
        loaded_model: LoadedModel,
        temperature: float = 0.7,
        max_tokens: int = 100,
    ) -> EvaluatorResult:
        """Run a single prompt through the loaded model.

        Generates sample text and computes loss/perplexity.

        Parameters
        ----------
        prompt : str
            The prompt text to evaluate.
        loaded_model : object
            A pre-loaded ``LoadedModel`` instance (from ``InferenceService.load_model``).
        temperature : float, optional
            Sampling temperature for generation. Defaults to 0.7.
        max_tokens : int, optional
            Maximum tokens to generate. Defaults to 100.

        Returns
        -------
        EvaluatorResult
            Generated text, average loss, and perplexity.
        """
        generated = self._inference.generate(
            loaded_model,
            prompt=prompt,
            temperature=temperature,
            max_tokens=max_tokens,
        )
        loss_result = self._inference.loss_breakdown(prompt, loaded_model)
        avg_loss = loss_result["average_loss"]
        perplexity = loss_result.get("perplexity", None)
        if perplexity is None:
            import math

            perplexity = math.exp(avg_loss)
        return EvaluatorResult(
            generated_text=generated,
            avg_loss=avg_loss,
            perplexity=perplexity,
        )
